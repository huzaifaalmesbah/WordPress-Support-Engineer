// Assignment Operator
var math1 = 5;
var math2 = 10;
var math3 = 20;
var math4 = 30;

console.log(math1);

// Addition Operator "+"
var adding = math1 + math2;
console.log("Total: " + adding);

// Subtraction "-"
var subtraction = adding + math3;
console.log("Subtraction: " + subtraction);

//  Multiplying "*"
var multiplay = adding * subtraction;
console.log("Multiplying: " + multiplay);

// Division "/"
var division = multiplay / math4;
console.log("Division: " + division);

//   Modulus  "%"

var modulus = division % math1;
console.log("Modulus: " + modulus);

// Increment "++"
var number = 10;
number++;
console.log("Increment: " + number);
// Decrement
number--;
console.log("Decrement: " + number);

console.log(math1 <= 5);
console.log(math1 <= 10);

//  Comparison Operators
// Equal to
console.log(10 == 20);
// Equal value and equal type
console.log(10 === 10);
// not equal
console.log(10 != 20);

// Greater than
console.log(10 > 20);

// Less than
console.log(10 < 20);

// Greater than or equal to
console.log(10 >= 20);

//  Less than or equal to
console.log(10 <= 20);
